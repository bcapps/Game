import UIKit

extension UIFont {
    
    static func headingFont() -> UIFont {
        return UIFont.boldSystemFontOfSize(16.0)
    }
    
    static func bodyFont() -> UIFont {
        return UIFont.systemFontOfSize(15.0)
    }
}
